                alert("Please verify the reCAPTCHA.");
                return false;
            }

            $("#submit_button").prop("disabled", true).text("SENDING...");
            $("#load_form").show();

            var formData = {
                name: $("#name").val(),
                lastName: $("#lastName").val(),
                phone: $("#phone").val(),
                email: $("#email").val(),
                message: $("#message").val(),
                "g-recaptcha-response": captchaResponse
            };

