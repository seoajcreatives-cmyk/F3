                    name: $("#name").val(),
                    last_name: $("#lastName").val(),
                    phone: $("#phone").val(),
                    email: $("#email").val(),
                    message: $("#message").val(),
                    privacy_consent: $("#privacy_consent").is(":checked")
                })
            }).catch(err => console.log("Zapier error:", err));

