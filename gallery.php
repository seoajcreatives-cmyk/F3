                modal_img.attributes.src.value = nextImg.attributes.src.value;
                modal_img.attributes.id.value = nextImg.attributes.id.value;
            })

            next.addEventListener('click', () => {
                const idImgAct = parseInt(modal_img.attributes.id.value);
                const nextImg = images[idImgAct + 1]
                modal_img.attributes.src.value = nextImg.attributes.src.value;
                modal_img.attributes.id.value = nextImg.attributes.id.value;
            })

            cerrar.addEventListener('click', () => {
                modal.style.display = "none";
            })

            images.forEach(item => {
                item.addEventListener('click', () => {
                    abrirModal(item.attributes.src.value, item.attributes.id.value);
                })
            })

            function abrirModal(src, id) {
                modal.style.display = "flex"
                modal_img.attributes.src.value = src;
                modal_img.attributes.id.value = id;


